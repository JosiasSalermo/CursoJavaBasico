package aula11;

public class VariaveisChar {

    public static void main(String[] args){

        char o = 'o';
        char i = 'i';

        System.out.println("o + i");
        System.out.println("" + o + i);

        char interrogacao = 0x3f;

        System.out.println("" + o + i + interrogacao);
    }
}